package jp.co.kaki.App;

/**
 * @author 216412
 *
 */
public class Argument {
	private int a;
	private int b;
	

	public Argument(int a, int b) {
		super();
		this.a = a;
		this.b = b;
	}
	
	/**
	 * @return a
	 */
	public int getA() {
		return a;
	}
	/**
	 * @param a セットする a
	 */
	public void setA(int a) {
		this.a = a;
	}
	/**
	 * @return b
	 */
	public int getB() {
		return b;
	}
	/**
	 * @param b セットする b
	 */
	public void setB(int b) {
		this.b = b;
	}

}
